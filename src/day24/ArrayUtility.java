package day24;

import java.util.*;


public class ArrayUtility {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in) ; 
	    int[] nums = new int[]{1,45,33,100,8} ; 
	    
	    System.out.println(nums);
	    
	    // Array is object that store multiple items of same type 
	    
	    // 
	    // Arrays is Utility class that comes with JDK just like scanner
	    // Arrays class in in java.util package 
	    // it provide lots of pre-defined methods to work with 
	    // array objects 
	    String str = Arrays.toString(nums) ; 
	    System.out.println( str  );
	    
	}

}
