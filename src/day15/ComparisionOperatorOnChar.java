package day15;

public class ComparisionOperatorOnChar {

	public static void main(String[] args) {
		
		char c1 = 'a';
		char c2 = 'B';
		
		System.out.println( c1==c2 );
		System.out.println( c1!=c2 );
		System.out.println( c1>=c2 );
		System.out.println( c1<c2 );
		System.out.println( c1<=c2 );

	}

}
